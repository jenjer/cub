#include "../headers/cub3d.h"

void	fill_sp_map(char *sp_map, int idx)
{
	int	i;

	i = 0;
	while (i < idx - 1)
	{
		sp_map[i] = 'X';
		i++;
	}
	sp_map[idx - 1] = '\0';
}
